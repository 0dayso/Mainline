vector<BYTE> PingYingHelper::GetPingYin(string str)
{
	vector<BYTE> vec;
	int nlen = str.length();
	bool bstart = false;
	string spy;
	string shz;
	for (int i = 0; i <= nlen; i++)
	{
		if (i < nlen - 1 && eTerm_00611928((BYTE)str[i], (BYTE)str[i + 1]))
		{
			if (!bstart)
			{
				bstart = true;
				spy = "";
				shz = "";
			}
			if (bstart)
			{
				char sz[] = { str[i], str[i + 1], 0x00 };
				auto it = HZ_PINGXIN.find(sz);
				if (it != HZ_PINGXIN.end())
					spy += it->second;				
				shz += string(sz);
				i++;
			}
		}
		else
		{
			if (bstart)
			{
				for (int j = 0; j < spy.length(); j++)
				{
					vec.push_back((BYTE)spy[j]);
				}
				vec.push_back(0x1B);
				vec.push_back(0x0E);
				for (int k = 0; k < shz.length(); k++)
				{
					BYTE bt1 = shz[k];
					BYTE bt2 = shz[++k];
					UsasToGb(bt1, bt2);
					vec.push_back((BYTE)bt1);
					vec.push_back((BYTE)bt2);
				}
				vec.push_back(0x1B);
				vec.push_back(0x0F);
				bstart = false;
				spy = "";
				shz = "";
			}
			if (i < nlen)
				vec.push_back((BYTE)str[i]);
		}
	}
	return vec;
}

