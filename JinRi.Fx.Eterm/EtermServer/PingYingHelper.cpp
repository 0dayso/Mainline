void PingYingHelper::UsasToGb(byte& c1, byte& c2)
{
	BYTE bt1 = c1;
	BYTE bt2 = c2;

	bt1 += 0x72;
	if (bt1 > 0x24)
	{
		bt1 += 0x0E;
	}

	bt2 += 0x80;
	if (bt2 >= 0x2F && bt2 <= 0x32)
	{
		bt2 -= 0x0A;
		c1 = bt2;
		c2 = bt1;
		return;
	}

	c1 = bt1;
	c2 = bt2;
}
